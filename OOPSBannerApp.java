/**
 * OOPSBannerApp prints OOPS using asterisks.
 */
public class OOPSBannerApp {

    /**
     * Main method - entry point of the application.
     * @param args command line arguments
     */
    public static void main(String[] args) {
        System.out.println(" ***   ***   ***   ***** ");
        System.out.println("*   * *   * *   *  *     ");
        System.out.println("*   * *   * *   *  ***** ");
        System.out.println("*   * *   * *          * ");
        System.out.println(" ***   ***  *      ***** ");
    }
}
